export default function CalculatePosition(i){
    if(i === 0){
        return ' A-1'
    }else if(i === 1){
        return ' B-1'
    }else if(i === 2){
        return ' C-1'
    }else if(i === 3){
        return ' A-2'
    }else if(i === 4){
        return ' B-2'
    }else if(i === 5){
        return ' C-2'
    }else if(i === 6){
        return ' A-3'
    }else if(i === 7){
        return ' B-3'
    }else{
        return ' C-3'
    }
}